package com.acpitzone.gulatikirasoi;

public class offers {

    String id, offerN, offerC;

    public offers(String id, String offerN, String offerC) {
        this.id = id;
        this.offerN = offerN;
        this.offerC = offerC;
    }

    public String getId() {
        return id;
    }

    public String getOfferN() {
        return offerN;
    }

    public String getOfferC() {
        return offerC;
    }
}
